const I18N_KEY = "dp_lang";

function applyLang(lang) {
  const dict = I18N[lang] || I18N.fr;
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (!(key in dict)) return;
    if (el.hasAttribute("data-i18n-html")) {
      el.innerHTML = dict[key];
    } else {
      el.textContent = dict[key];
    }
  });
  // attr-based translations: data-i18n-attr="aria-label" + data-i18n-attr-key
  document.querySelectorAll("[data-i18n-attr]").forEach(el => {
    const attr = el.getAttribute("data-i18n-attr");
    const key  = el.getAttribute("data-i18n-attr-key");
    if (!attr || !key || !(key in dict)) return;
    el.setAttribute(attr, dict[key]);
  });
  // Titre de page
  if (dict["hero.eyebrow"]) {
    const baseTitle = "Delfosse Properties — ";
    document.title = baseTitle + dict["hero.eyebrow"];
  }
  // Chips actifs
  document.querySelectorAll(".lang-switch [data-lang]").forEach(b => {
    b.classList.toggle("is-active", b.dataset.lang === lang);
  });
  localStorage.setItem(I18N_KEY, lang);
}

function initI18n() {
  const saved = localStorage.getItem(I18N_KEY);
  const browser = (navigator.language || "fr").slice(0,2).toLowerCase();
  const initial = saved || (I18N[browser] ? browser : "fr");
  applyLang(initial);

  document.querySelectorAll(".lang-switch [data-lang]").forEach(btn => {
    btn.addEventListener("click", () => applyLang(btn.dataset.lang));
  });
}

document.addEventListener("DOMContentLoaded", initI18n);

// Helpers exposés pour le JS : t(key) = texte traduit courant · lang() = code courant
function currentLang(){
  return document.documentElement.lang || localStorage.getItem(I18N_KEY) || "fr";
}
function t(key){
  const lang = currentLang();
  const dict = I18N[lang] || I18N.fr;
  return (key in dict) ? dict[key] : (I18N.fr[key] || key);
}
window.DP_I18N = { apply: applyLang, t: t, lang: currentLang };
